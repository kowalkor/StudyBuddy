    public void reset() {
        String temp = "";

        for (int i = 0; i < this.questionTextArea.getText().length(); i++) {
            temp.concat(" ");
        }
        this.questionTextArea.setText(temp);

        for (int i = 0; i < this.answerTextArea.getText().length(); i++) {
            temp.concat(" ");
        }
        this.answerTextArea.setText(temp);
    }

    public boolean hasNoText() {
        boolean isEmpty = false;
        if (this.questionTextArea.getText().isEmpty() || this.answerTextArea.getText().isEmpty()) {
            isEmpty = true;
        }
        return isEmpty;
    }

